                    <h2>Team News</h2>

                    <p>Aside from Green's return, United has a fully fit squad to choose from. Young defender Tom Williams, who made his debut last month, is pushing for a starting spot after impressive performances in recent weeks.</p>

                    <p>City will be without their suspended midfielder Paul Rodriguez but otherwise have no injury concerns. Their star striker Carlos Mendez has scored in each of his last five appearances and will be the main threat to United's defense.</p>

                    <div class="inline-image right">
                        <img src="https://placehold.co/600x400.png" alt="Defensive training">
                        <p class="caption">Defensive drills during training</p>
                    </div>

                    <p>Weather forecasts predict sunny conditions for matchday, which should suit United's preferred style of quick, passing football. The pitch has been in excellent condition all season thanks to the groundstaff's efforts.</p>

                    <p>Fans are reminded to arrive early due to enhanced security checks, with gates opening two hours before kickoff. The club has also arranged additional public transport services to help with the expected large crowds.</p>

